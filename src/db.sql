CREATE TABLE IF NOT EXISTS Livros (
  isbn CHAR(13) PRIMARY KEY,
  nome VARCHAR(100) NOT NULL,
  autor VARCHAR(100) NOT NULL,
  genero VARCHAR(20) NOT NULL
);

CREATE TABLE IF NOT EXISTS Admins (
  email VARCHAR(50) PRIMARY KEY,
  nome VARCHAR(100) NOT NULL,
  senha VARCHAR(100) NOT NULL
);

CREATE TABLE IF NOT EXISTS Leitores (
  email VARCHAR(50) PRIMARY KEY,
  nome VARCHAR(100) NOT NULL,
  senha VARCHAR(100) NOT NULL,
  qtdEmprestimos INT NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS Emprestimos (
  emailLeitor VARCHAR(50) NOT NULL,
  idEmprestimo INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  isbnLivro CHAR(13) NOT NULL
);

CREATE TABLE IF NOT EXISTS ModificacoesLivros (
  idModificacao INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  emailAdmin VARCHAR(50) NOT NULL,
  isbn CHAR(13) NOT NULL
);

CREATE TABLE IF NOT EXISTS ModificacoesLeitor (
  idModificacao INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  emailAdmin VARCHAR(50) NOT NULL,
  isbn CHAR(13) NOT NULL
);
